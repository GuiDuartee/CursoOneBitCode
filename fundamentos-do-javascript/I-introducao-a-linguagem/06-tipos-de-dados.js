/*

Tipos primitivos

number    = tipo numérico tradicional, funciona tanto para números inteiros quanto para decimais
bigint    = utilizado para números inteiros muito grandes (basta colocar no final do número a letra n)
string    = tipo texto, pode ser utilizado aspas simples ou duplas, pode ser considerada uma lista de caracteres
boolean   = tipo lógico, pode ser true ou false
null      = tipo nulo, não possui valor algum mas é diferente de undefined, pis em algum momento foi definido
undefined = tipo indefinido, não possui valor algum e nunca foi definido

Objetos e funções são considerados tipos de referência, e não tipos primitivos.

*/
